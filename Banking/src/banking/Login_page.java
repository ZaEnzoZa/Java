    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Boolean michaelLogin = false;
        Boolean alexLogin = false;
        Boolean aidanLogin = false;
        Boolean zikhonaLogin = false;
        Boolean commonLogin = false;
        
        if (edtUsername.getText().equals("Michael") && edtPassword.getText().equals("MichaelPassword")) {
            michaelLogin = true;
            commonLogin = true;
        } else if (edtUsername.getText().equals("Alex") && edtPassword.getText().equals("AlexPassword")) {
            alexLogin = true;
            commonLogin = true;
        } else if (edtUsername.getText().equals("Aidan") && edtPassword.getText().equals("AidanPassword")) {
            aidanLogin = true;
            commonLogin = true;
        } else if (edtUsername.getText().equals("Zikhona") && edtPassword.getText().equals("ZikhonaPassword")) {
            zikhonaLogin = true;
            commonLogin = true;
        }
        if (commonLogin == false) {
            JOptionPane.showMessageDialog(null, "Incorrect Username or Password");            
        }else{
            new Banking_System().setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

